import Foundation

class Character {
    let name: String
    let age: String
    let height: String
    let favoriteColor: String
    let favoriteFood: String
    
    init(name: String, age: String, height: String, favoriteColor: String, favoriteFood: String) {
        self.name = name
        self.age = age
        self.height = height
        self.favoriteColor = favoriteColor
        self.favoriteFood = favoriteFood
    }
}
